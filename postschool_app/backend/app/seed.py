def seed(db: Session) -> None:
    if db.query(models.University).first():
        return  # already seeded

    subjects = {}
    for key, name in SUBJECTS:
        s = models.ExamSubject(key=key, name=name)
        db.add(s)
        subjects[key] = s

    cities = {}
    for name, desc, pop, clim, life in CITIES:
        c = models.City(name=name, description=desc, population=pop, climate=clim, student_life=life)
        db.add(c)
        cities[name] = c

    unis = {}
    for slug, short, full, city, price, mil, exams in UNIVERSITIES:
        u = models.University(
            slug=slug, short_name=short, full_name=full, city=cities[city],
            tuition_month=price, military_dept=mil, internal_exams=exams,
        )
        db.add(u)
        unis[slug] = u

    for slug, name, code, thr, subj_keys in PROGRAMS:
        p = models.Program(university=unis[slug], name=name, code=code, threshold=thr)
        p.subjects = [subjects[k] for k in subj_keys]
        db.add(p)

    for title, meta, bonus in OLYMPIADS:
        db.add(models.Olympiad(title=title, meta=meta, bonus=bonus))

    db.commit()
